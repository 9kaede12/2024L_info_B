"""
変数で文字列を扱う
    変数で文字列を扱うことが出来ます。

"""
job = "見習い剣士" #jobという変数を宣言し、「見習い剣士」という文字列を入れる
print("あなたの職業は"+job) # 「あなたの職業は」とjobの値と繋げて出力
print("クラスチェンジした！") # 「クラスチェンジした！」と出力
job="駆け出し勇者" # jobに新たな文字列を代入
print("あなたの職業は"+job) # 「あなたの職業は」とjobの値と繋げて出力

a = "文字列" * 2 # 「文字列」２回連続して代入する
print(a) # 「文字列」を２回連続して出力